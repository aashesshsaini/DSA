const arr = [2, 3, 1, 5, 4];

const bubbleSortAlgo = (arr) => {
  for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr.length - i - 1; j++) {
      if (arr[j] > arr[j + 1]) {
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
      }
    }
  }
  return arr;
};

const result = bubbleSortAlgo(arr);
console.log(result);
